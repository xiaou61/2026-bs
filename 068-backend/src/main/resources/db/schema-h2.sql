DROP TABLE IF EXISTS user_complaint;
DROP TABLE IF EXISTS travel_review;
DROP TABLE IF EXISTS travel_order;
DROP TABLE IF EXISTS user_favorite;
DROP TABLE IF EXISTS traveler;
DROP TABLE IF EXISTS scenic_spot;
DROP TABLE IF EXISTS sys_user;

CREATE TABLE sys_user (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username VARCHAR(50) NOT NULL,
  password VARCHAR(100) NOT NULL,
  nickname VARCHAR(50) NOT NULL,
  phone VARCHAR(20) DEFAULT '',
  email VARCHAR(100) DEFAULT '',
  avatar VARCHAR(255) DEFAULT '',
  profile VARCHAR(500) DEFAULT '',
  role VARCHAR(20) NOT NULL,
  status TINYINT NOT NULL DEFAULT 1,
  last_login_time DATETIME NULL,
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uk_username UNIQUE (username)
);

CREATE TABLE scenic_spot (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  city VARCHAR(50) NOT NULL,
  tags VARCHAR(255) DEFAULT '',
  price DECIMAL(10,2) NOT NULL DEFAULT 0,
  cover CLOB,
  intro VARCHAR(1000) DEFAULT '',
  status TINYINT NOT NULL DEFAULT 1,
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE traveler (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  name VARCHAR(50) NOT NULL,
  cert_type VARCHAR(20) NOT NULL DEFAULT '身份证',
  cert_no VARCHAR(50) NOT NULL,
  phone VARCHAR(20) NOT NULL,
  is_default TINYINT NOT NULL DEFAULT 0,
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_traveler_user_id ON traveler (user_id);

CREATE TABLE user_favorite (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  spot_id BIGINT NOT NULL,
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uk_user_spot UNIQUE (user_id, spot_id)
);

CREATE INDEX idx_favorite_user_id ON user_favorite (user_id);

CREATE TABLE travel_order (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_no VARCHAR(40) NOT NULL,
  user_id BIGINT NOT NULL,
  traveler_id BIGINT NOT NULL,
  spot_id BIGINT NOT NULL,
  travel_date DATE NOT NULL,
  quantity INT NOT NULL DEFAULT 1,
  total_amount DECIMAL(10,2) NOT NULL DEFAULT 0,
  status VARCHAR(20) NOT NULL,
  contact_name VARCHAR(50) NOT NULL,
  contact_phone VARCHAR(20) NOT NULL,
  remark VARCHAR(500) DEFAULT '',
  pay_time DATETIME NULL,
  finish_time DATETIME NULL,
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uk_order_no UNIQUE (order_no)
);

CREATE INDEX idx_order_user_id ON travel_order (user_id);
CREATE INDEX idx_order_spot_id ON travel_order (spot_id);

CREATE TABLE travel_review (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id BIGINT NOT NULL,
  user_id BIGINT NOT NULL,
  spot_id BIGINT NOT NULL,
  score TINYINT NOT NULL,
  content VARCHAR(1000) NOT NULL,
  status TINYINT NOT NULL DEFAULT 1,
  reply_content VARCHAR(1000) DEFAULT '',
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uk_review_order_id UNIQUE (order_id)
);

CREATE INDEX idx_review_spot_id ON travel_review (spot_id);

CREATE TABLE user_complaint (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  order_id BIGINT NOT NULL,
  type VARCHAR(50) NOT NULL,
  content VARCHAR(1000) NOT NULL,
  attachment_urls CLOB,
  status VARCHAR(20) NOT NULL DEFAULT 'WAITING',
  handle_result VARCHAR(1000) DEFAULT '',
  handle_by BIGINT NULL,
  handle_time DATETIME NULL,
  create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_complaint_user_id ON user_complaint (user_id);
CREATE INDEX idx_complaint_status ON user_complaint (status);

DROP TABLE IF EXISTS campus_notice;
DROP TABLE IF EXISTS lost_found;
DROP TABLE IF EXISTS job_apply;
DROP TABLE IF EXISTS intern_job;
DROP TABLE IF EXISTS activity_signup;
DROP TABLE IF EXISTS campus_activity;
DROP TABLE IF EXISTS course_enroll;
DROP TABLE IF EXISTS campus_course;
DROP TABLE IF EXISTS sys_user;

CREATE TABLE sys_user (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username VARCHAR(50) NOT NULL UNIQUE,
  password VARCHAR(100) NOT NULL,
  nickname VARCHAR(50) NOT NULL,
  phone VARCHAR(20) DEFAULT '',
  email VARCHAR(100) DEFAULT '',
  role VARCHAR(20) NOT NULL,
  status TINYINT NOT NULL DEFAULT 1,
  college VARCHAR(100) DEFAULT '',
  major VARCHAR(100) DEFAULT '',
  grade VARCHAR(20) DEFAULT '',
  last_login_time TIMESTAMP NULL,
  create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE campus_course (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(120) NOT NULL,
  teacher_id BIGINT NOT NULL,
  credit DECIMAL(4,1) NOT NULL DEFAULT 2.0,
  location VARCHAR(120) DEFAULT '',
  max_student INT NOT NULL DEFAULT 60,
  selected_count INT NOT NULL DEFAULT 0,
  start_date DATE NULL,
  end_date DATE NULL,
  status TINYINT NOT NULL DEFAULT 1,
  description CLOB,
  create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE course_enroll (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  course_id BIGINT NOT NULL,
  student_id BIGINT NOT NULL,
  status TINYINT NOT NULL DEFAULT 1,
  score DECIMAL(5,2) DEFAULT NULL,
  remark VARCHAR(255) DEFAULT '',
  create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uk_course_student UNIQUE(course_id, student_id)
);

CREATE TABLE campus_activity (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title VARCHAR(120) NOT NULL,
  organizer_id BIGINT NOT NULL,
  location VARCHAR(120) DEFAULT '',
  start_time TIMESTAMP NULL,
  end_time TIMESTAMP NULL,
  max_participant INT NOT NULL DEFAULT 200,
  participant_count INT NOT NULL DEFAULT 0,
  status TINYINT NOT NULL DEFAULT 1,
  content CLOB,
  create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE activity_signup (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  activity_id BIGINT NOT NULL,
  student_id BIGINT NOT NULL,
  status TINYINT NOT NULL DEFAULT 1,
  checkin_time TIMESTAMP NULL,
  remark VARCHAR(255) DEFAULT '',
  create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uk_activity_student UNIQUE(activity_id, student_id)
);

CREATE TABLE intern_job (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title VARCHAR(120) NOT NULL,
  company VARCHAR(120) NOT NULL,
  city VARCHAR(80) DEFAULT '',
  salary VARCHAR(80) DEFAULT '',
  deadline DATE NULL,
  publisher_id BIGINT NOT NULL,
  status TINYINT NOT NULL DEFAULT 1,
  description CLOB,
  create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE job_apply (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  job_id BIGINT NOT NULL,
  student_id BIGINT NOT NULL,
  resume_url VARCHAR(255) DEFAULT '',
  status TINYINT NOT NULL DEFAULT 0,
  apply_note VARCHAR(255) DEFAULT '',
  review_note VARCHAR(255) DEFAULT '',
  reviewer_id BIGINT NULL,
  create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uk_job_student UNIQUE(job_id, student_id)
);

CREATE TABLE lost_found (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  type TINYINT NOT NULL,
  title VARCHAR(120) NOT NULL,
  item_name VARCHAR(120) NOT NULL,
  contact VARCHAR(120) NOT NULL,
  location VARCHAR(120) DEFAULT '',
  happen_time TIMESTAMP NULL,
  description CLOB,
  image_url VARCHAR(255) DEFAULT '',
  status TINYINT NOT NULL DEFAULT 1,
  publisher_id BIGINT NOT NULL,
  create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE campus_notice (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title VARCHAR(120) NOT NULL,
  content CLOB,
  publisher_id BIGINT NOT NULL,
  status TINYINT NOT NULL DEFAULT 1,
  create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
